<h1 class="mission">Honor Ceremonies Include</h1>
<p class="services">
	<ul >
		<li>Nursing dress in the traditional white uniform with cape, cap, white gloves, and white shoes</li>
		<li>Nurse tribute is read</li>
		<li>A nightingale lamp will be presented to the family in honor of their loved one</li>
		<li>A white rose is placed on the casket, near the urn or presented to the family which exemplifies
			honor and appreciation for the nurse's devotion to their chosen profession</li>
		<li>Placement of a blue carnation on the casket, near the urn, or presented to the family symbolizes
			peace and serenity</li>
		<li>"Final call for duty"</li>
		<li>The Nurses prayer</li>
	</ul>
</p>
