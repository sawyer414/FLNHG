	<h1><em>Our Mission</em></h1>
	<p class="missionP">The Finger Lakes Honor Guard is priviliged to recognize and honor those who have dedicated<br>
	their lives to serving others in their chosen profession as a nurse at the end of their life journey<br> the
	honor guard's presence provides meaningful recognition and honor to a nurse who has passed,<br> and in hopes of 
	easing the grief of their family members, loved ones and colleagues.</p>
	<br><br><br>
	<h1><em>Our Vision</em></h1>
	<p class="missionP">To honor nurses at the end-of-life journey while recognizing their contributions to their 
	profession as a nurse.<br> This is achieved through a group of compassionate and devoted volunteer nurses coming 
	together to perform<br> a tribute ceremony that perpetuates the legacy of nursing, offers comfort to their grieving
	families,<br> and inspires future generations of nurses by highlighting the values of compassion, dedication,<br> and
	service within the nursing profession.</p>
