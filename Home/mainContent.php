			<h1 align="center"><b>Welcome to the Finger Lakes Nurse Honor Guard</b></h1><br><br>
			
			The Finger Lakes Nurse Honor Guard is a group of volunteer nurses dedicated to recognizing 
			and honoring nurses in their last season of their life and at the end of their life journey.
			Through our brief yet unique and personalized Nightingale Tribute, we celebrate the service 
			and dedication of nurses who have cared for their communities with compassion and commitment.
			<br><br>
			
			Founded in October 2023, our organization has grown to include over 50 volunteer members. 
			We are a not-for-profit organization with 501c3 status.<br><br>
			
			Members are licensed practical nurses (LPNs), registered nurses (RNs), and Nurse Practioners 
			(NP) who work or have worked in all areas of their nusing profession. Our volunteers serve 
			across Ontario, Wayne, Seneca and Yates counties.
			<br><br>
			
			From October 2023 to April 2026, we honored the nursing profession for 60 nurses across our 4-county area. 
			Our mission to ensure that every nurse--LPN, RN, or NP, regardless of gender or work status
			-- recieves the recognition they deserve for their dedication to their profession as a nurse.
			<br><br>
			
			Explore our website to learn more about requesting a tribute ceremony for your loved one, 
			joining our organization, or donating to support our mission. Together we carry forward the 
			legacy of nusing with the dignity and the respect it so richly deserves.<br>
